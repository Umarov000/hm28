const { Schema, model } = require("mongoose");

const clientSchema = new Schema({
  full_name: {
    type: String,
    required: true,
  },
  phone: {
    type: Number,
    required: true,
  },
  address: {
    type: String,
    required: true,
  },
  location: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
});


module.exports = model("Client", clientSchema);
